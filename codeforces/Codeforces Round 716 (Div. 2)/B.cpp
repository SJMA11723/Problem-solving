#include <bits/stdc++.h>

using namespace std;

const int MOD = 1e9 + 7;

int main(){
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    int t; cin >> t;
    while(t--){
        int n, k; cin >> n >> k;
        int ans = 1;
        for(int i = 0; i < k; ++i) ans = 1ll * ans * n % MOD;
        cout << ans << '\n';
    }
}
